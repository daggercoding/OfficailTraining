function maxProduct(grid){
    let largestProduct=0;
    for(row=0; row <=19 ; row++){
        for(col=0; col<=19; col++){
        if(col+3<20){
            let virtical=grid[row][col]*grid[row][col+1]*grid[row][col+2]*grid[row][col+3]
            largestProduct= Math.max(largestProduct,virtical)
        }
        if(row+3<20){
            let horizontal=grid[row][col]*grid[row+1][col]*grid[row+2][col]*grid[row+3][col]
            largestProduct= Math.max(largestProduct,horizontal)
        }
        if(row+3<20 && col+3<20){
            let diagonal=grid[row][col]*grid[row+1][col+1]*grid[row+2][col+2]*grid[row+3][col+3]
            largestProduct= Math.max(largestProduct,diagonal)
        }
        if(row-3>=0&&col+3<20){
        let diagonal2=grid[row][col]*grid[row-1][col+1]*grid[row-2][col+2]*grid[row-3][col+3]
        largestProduct= Math.max(largestProduct,diagonal2) 
        }
        }
    }
    return largestProduct;
}
