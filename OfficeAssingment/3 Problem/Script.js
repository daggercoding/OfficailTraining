//================================>>>>>GENERATING 2D ARRAY
let grid = [];

for (let i = 0; i < 20; i++) {
  let col = [];
  for (let j = 0; j < 20; j++) {
    col.push(Math.floor(Math.random()* 10));
  }
  grid.push(col);
}

document.getElementById("data").innerText=JSON.stringify(grid)

let gridLocation;
let direction="";
let max=0

for (let row = 0; row < grid.length; row++) {
for (let col = 0; col < grid.length ; col++) {

  if (row > 0 && row < grid.length-1 && col > 0 && col < grid.length-1) {

    let verticalHorizontal = grid[row][col] * grid[row][col+1] *grid[row][col-1] *grid[row+1][col] *grid[row-1][col];

    let diagonal = grid[row][col] *grid[row+1][col+1] *grid[row-1][col-1] *grid[row+1][col-1] *grid[row-1][col+1];

    let[maxProduct,directions] = maxGrid(verticalHorizontal, diagonal);
    if(max<maxProduct)
    {
      max=maxProduct
      gridLocation=`Row:${row+1} Col:${col+1}`
      direction=directions
    }

  }
}
}

document.getElementById("result").innerText=(`Maximum Product is : ${max} || Element location is : ${gridLocation} || Direction is : ${direction}`);

function maxGrid(verticalHorizontal,diagonal) {
  if (verticalHorizontal > diagonal) {
      return [verticalHorizontal,"verticalHorizontal"];
  } else {
      return [diagonal,"diagonal"];
  }
}





